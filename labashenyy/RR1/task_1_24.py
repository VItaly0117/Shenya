# --- ДЕМОНСТРАЦІЯ ГЛОБАЛЬНИХ ЗМІННИХ ---
"""
Функція розраховує прискорення вільного падіння.
Demonstrates: Використання глобальної змінної всередині функції
та створення локальної змінної.
"""
# Глобальна змінна (константа), видима у всій програмі
GRAVITATIONAL_CONSTANT = 6.7e-8  # G у г^-1 * см^3 * сек^-2

def calculate_gravity(mass, radius_km):
    # Локальна змінна (існує тільки в межах цієї функції)
    radius_cm = radius_km * 100000  # Переведення км у см
    # Використання глобальної змінної GRAVITATIONAL_CONSTANT у формулі
    g = (GRAVITATIONAL_CONSTANT * mass) / (radius_cm ** 2)
    return g
if __name__ == "__main__":
    print("--- Задача 1.24: Гравітація (Глобальні змінні) ---")
    # Венера: m=4.86e27 г, r=6175 км
    g_venus = calculate_gravity(4.86e27, 6175)
    # Сатурн: m=5.68e29 г, r=57750 км
    g_saturn = calculate_gravity(5.68e29, 57750)
    print(f"Прискорення на Венері: {g_venus:.2f} см/с^2")
    print(f"Прискорення на Сатурні: {g_saturn:.2f} см/с^2")
    if g_saturn > g_venus:
        print("Висновок: Сатурн має більше прискорення.")
    else:
        print("Висновок: Венера має більше прискорення.")