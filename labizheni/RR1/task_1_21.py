    # Формула з умови: r = a * sqrt(3) / 3
    calculated_radius = side_a * math.sqrt(3) / 3
    # Логіка задачі: чи поміститься трикутник у колі з таким радіусом?
    # Оскільки радіус розраховано саме для цього трикутника, він ідеально вписується.
    is_fitting = True
    return calculated_radius, is_fitting
